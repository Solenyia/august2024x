public class Main {

    public static void main(String[] args) {
        int myNumber; //deklaracia premennej
        // integer ma 32bitov

        short myShort = 32;
        // short ma 16bitov

        long myLong = 9090;
        // long ma 64bitov

        double myDouble = 12.552;

        float myFloat = 3.14f;
        // float v Jave musi mat na konci f, inak je to double

        char myChar = 'y';
        // vsetky znaky

        boolean myBoolean = true;
        // Trure/False

        byte myByte = 127;
        //max 0-127 (8bitov)

        myNumber = 123; //inicializacia premennej
        System.out.println(myNumber);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBoolean);
        System.out.println(myByte);

        //Pokracovanie


    }
}